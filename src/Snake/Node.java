public class Node {
	private int nodeX,nodeY;
	public Node(int X,int Y) {
		this.nodeX=X;
		this.nodeY=Y;
	}
	
	public int getNodeX() {
		return nodeX;
	}
	public void setNodeX(int nodeX) {
		this.nodeX=nodeX;
	}
	
	public int getNodeY() {
		return nodeY;
	}
	public void setNodeY(int nodeY) {
		this.nodeY=nodeY;
	}
}
